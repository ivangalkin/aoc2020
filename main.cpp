    size_t mult = 1;

    for (auto [inc_col, inc_row] : incs) {
        size_t source_row = 0, target_row, final_row = (sizeof(input) / sizeof(std::string_view)) -1;
        size_t source_col= 0, target_col;
        size_t counter = 0;
        do {
            target_row = source_row + inc_row;

            if (target_row > final_row) {
                break;
            }

            target_col = (source_col + inc_col) % input[target_row].size();

            assert(target_row <= final_row);
            assert(target_col < input[target_row].size());

            if (input[target_row][target_col] == '#') {
                ++counter;
            }

            std::swap(source_col, target_col);
            std::swap(source_row, target_row);
        } while (true);
        std::cout << counter << std::endl;
        mult *= counter;
    }

    std::cout << mult << std::endl;

    return 0;
